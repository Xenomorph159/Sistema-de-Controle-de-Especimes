    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        boolean status = true;

        String s = jTextField5.getText();
        this.codigo = Integer.parseInt(s);

        String a = jTextField4.getText();
        this.senha = a;

        if (Classes.FuncionarioDAO.getInstance().pesquisar(this.codigo) != null) {
            if (this.senha.equals(Classes.FuncionarioDAO.getInstance().getSenha(this.codigo))) {
                Painel_Principal pp = new Painel_Principal();
                pp.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Reveja seus dados !", "ERRO", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

