    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        boolean status = true;

        String s = jTextField2.getText();

        String a = jTextField3.getText();
        this.codigo = Integer.parseInt(a);

        if (status) {
            if (s.equals(Classes.Especime.Estagio.ADULTO.getStringEstagio())) {
                this.estagio = Classes.Especime.Estagio.ADULTO;
            }
            if (s.equals(Classes.Especime.Estagio.JOVEM.getStringEstagio())) {
                this.estagio = Classes.Especime.Estagio.JOVEM;
            }
            if (s.equals(Classes.Especime.Estagio.BEBE.getStringEstagio())) {
                this.estagio = Classes.Especime.Estagio.BEBE;
            }

            Classes.EspecimeDAO.getInstance().atualizaEstagio(this.codigo, this.estagio);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Não foi possivel alterar o estágio!", "ERRO", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

